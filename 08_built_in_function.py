#  1. abs() – Absolute value
print(abs(-7))   # 7

# 2. pow() – Power (x^y)
print(pow(2, 3)) # 8

# 3. round() – Rounds a number
print(round(3.456, 2))  # 3.46

#  4. min() / max() – Minimum / Maximum
print(min(1, 4, 2))     # 1
print(max(1, 4, 2))     # 4

# sum() – Sum of all elements
print(sum([1, 2, 3, 4]))  # 10

# 6. len() – Length of an object
print(len("Python"))   # 6

# 7. sorted() – Sorts an iterable
print(sorted([4, 1, 3, 2]))  # [1, 2, 3, 4]

#  8. type() – Returns the type
print(type(123))  # <class 'int'>

# int() / float() / str() – Type casting
print(int(3.8))     # 3
print(float("4.2")) # 4.2
print(str(100))     # '100'

# 10. divmod() – Returns (quotient, remainder)
print(divmod(10, 3))  # (3, 1)

