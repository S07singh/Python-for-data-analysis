# 1. Arithmetic operators
a = 10
b = 3

print(a + b)  # Addition: 13
print(a - b)  # Subtraction: 7
print(a * b)  # Multiplication: 30
print(a / b)  # Division: 3.333...
print(a % b)  # Modulus: 1
print(a ** b) # Exponentiation: 1000
print(a // b) # Floor Division: 3

# 2. Comparison (Relational) Operators

print(a > b)   # True
print(a < b)   # False
print(a == b)  # False
print(a != b)  # True
print(a >= b)  # True
print(a <= b)  # False

# 3. Logical Operators

x = True
y = False

print(x and y) # False
print(x or y)  # True
print(not x)   # False

# Assignment Operators

x = 5
x += 3  # x = x + 3 → 8
x *= 2  # x = x * 2 → 16

# 5. Bitwise Operators

a = 5      # 0101
b = 3      # 0011

print(a & b)  # AND → 1
print(a | b)  # OR  → 7
print(a ^ b)  # XOR → 6
print(~a)     # NOT → -6
print(a << 1) # Left Shift → 10
print(a >> 1) # Right Shift → 2
